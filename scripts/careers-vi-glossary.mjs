function glossaryFallback(text) {
  if (shouldSkipTranslation(text)) return text;
  const { result: protectedText, tokens } = protectTerms(text);
  const words = protectedText.split(/(\s+|[.,;:!?()—–\-/→&]+)/);
  const translated = words
    .map((part) => {
      if (!part || /^\s+$/.test(part) || /^[.,;:!?()—–\-/→&]+$/.test(part)) return part;
      const lower = part.toLowerCase();
      if (WORD_GLOSSARY[lower] !== undefined) {
        if (!WORD_GLOSSARY[lower]) return "";
        return part[0] === part[0].toUpperCase()
          ? WORD_GLOSSARY[lower].charAt(0).toUpperCase() + WORD_GLOSSARY[lower].slice(1)
          : WORD_GLOSSARY[lower];
      }
      return part;
    })
    .join("")
    .replace(/\s+/g, " ")
    .trim();
  const restored = restoreTerms(translated, tokens);
  return restored !== text ? restored : text;
}

export function translateTree(value, exactMap = {}) {
  if (typeof value === "string") {
    return translateCareersString(value, exactMap);
  }
  if (Array.isArray(value)) {
    return value.map((item) => translateTree(item, exactMap));
  }
  if (value && typeof value === "object") {
    const out = {};
    for (const [k, v] of Object.entries(value)) {
      out[k] = translateTree(v, exactMap);
    }
    return out;
  }
  return value;
}
